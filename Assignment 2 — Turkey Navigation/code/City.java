/**
 * City class with data fields city name, x and y
 * representing the city's name, city's x coordinate and city's y coordinate, respectively.
 */
public class City {
    public String cityName; // name of the object
    public int x; // x coordinate of the object
    public int y; // y coordinate of the object

    // constructor
    public City(String cityName, int x, int y) {
        this.cityName = cityName;
        this.x = x;
        this.y = y;
    }
}
